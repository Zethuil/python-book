# simple integers can be literals or results of math operations
print(-9)
print(1139)
print(5 + 9)
print((21 - 3) * 9 + 6)

# 'simple' division will always return a float and not an int
print(3 / 2)
print(2 / 3)
print(4 / 4)

# for integer division use the double slash '//' operator
print(3 // 2)
print(2 // 3)
